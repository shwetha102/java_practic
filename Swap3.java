//write a java program to swap 2 members with using third variable
class Swap3{
  public static void main(String[] args){
    int a, b,temp;
    a=10;
    b=20;
    temp = a;
    a = b;
    b = temp;
    System.out.println("swap two numbers a"+a);
    System.out.println("swap two numbers b"+b);
   
  }
}